#region Helper Functions

function Initialize-RestorationLog {
    param([string]$LogPath)
    
    $logHeader = @"
╔════════════════════════════════════════════════════════════════════════╗
║                    RESTORATION OPERATION LOG                           ║
║                    $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')                          ║
╚════════════════════════════════════════════════════════════════════════╝

"@
    
    Set-Content -Path $LogPath -Value $logHeader -Force
    return $LogPath
}

function Write-RestoreLog {
    param(
        [string]$Message,
        [ValidateSet('Info', 'Warning', 'Error', 'Success', 'Header', 'Stat')]
        [string]$Level = 'Info',
        [string]$LogPath
    )
    
    $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $logMessage = "[$timestamp] [$Level] $Message"
    
    switch ($Level) {
        'Info'    { Write-Host $logMessage -ForegroundColor Cyan }
        'Warning' { Write-Host $logMessage -ForegroundColor Yellow }
        'Error'   { Write-Host $logMessage -ForegroundColor Red }
        'Success' { Write-Host $logMessage -ForegroundColor Green }
        'Header'  { Write-Host "`n$Message" -ForegroundColor Magenta }
        'Stat'    { Write-Host $Message -ForegroundColor White }
    }
    
    Add-Content -Path $LogPath -Value $logMessage
}

function Remap-DriveLetter {
    param(
        [string]$Path,
        [hashtable]$Mapping
    )
    
    if ($Mapping.Count -eq 0) {
        return $Path
    }
    
    $drive = Split-Path $Path -Qualifier
    
    if ($Mapping.ContainsKey($drive)) {
        $newDrive = $Mapping[$drive]
        $pathWithoutDrive = $Path.Substring($drive.Length)
        return $newDrive + $pathWithoutDrive
    }
    
    return $Path
}

function Test-PathCoherence {
    param(
        [string]$SourcePath,
        [string]$DestinationPath
    )
    
    $issues = @()
    
    # Check if source exists
    if (-not (Test-Path $SourcePath)) {
        $issues += "Source file does not exist: $SourcePath"
    }
    
    # Check if destination directory would be valid
    $destDir = Split-Path $DestinationPath -Parent
    $destDrive = Split-Path $DestinationPath -Qualifier
    
    if ($destDrive -and -not (Test-Path $destDrive)) {
        $issues += "Destination drive does not exist: $destDrive"
    }
    
    # Check for path length
    if ($DestinationPath.Length -gt 260) {
        $issues += "Destination path exceeds maximum length (260 chars): $DestinationPath"
    }
    
    return $issues
}

function Get-DriveLetterChanges {
    Write-Host "`n╔════════════════════════════════════════════════════════╗" -ForegroundColor Yellow
    Write-Host "║          DRIVE LETTER CHANGE DETECTION                ║" -ForegroundColor Yellow
    Write-Host "╚════════════════════════════════════════════════════════╝`n" -ForegroundColor Yellow
    
    Write-Host "Have any drive letters changed since the backup? (Y/N): " -NoNewline -ForegroundColor Cyan
    $response = Read-Host
    
    if ($response -eq 'Y' -or $response -eq 'y') {
        $mapping = @{}
        
        Write-Host "`nEnter drive letter mappings (press Enter when done):" -ForegroundColor Yellow
        Write-Host "Format: OldDrive NewDrive (e.g., 'D: E:')" -ForegroundColor DarkGray
        
        while ($true) {
            Write-Host "`nMapping (or press Enter to finish): " -NoNewline -ForegroundColor Cyan
            $input = Read-Host
            
            if ([string]::IsNullOrWhiteSpace($input)) {
                break
            }
            
            $parts = $input -split '\s+'
            if ($parts.Count -eq 2) {
                $oldDrive = $parts[0].ToUpper()
                $newDrive = $parts[1].ToUpper()
                
                # Ensure format is correct
                if (-not $oldDrive.EndsWith(':')) { $oldDrive += ':' }
                if (-not $newDrive.EndsWith(':')) { $newDrive += ':' }
                
                $mapping[$oldDrive] = $newDrive
                Write-Host "  Added mapping: $oldDrive -> $newDrive" -ForegroundColor Green
            } else {
                Write-Host "  Invalid format. Use: OldDrive NewDrive" -ForegroundColor Red
            }
        }
        
        return $mapping
    }
    
    return @{}
}

