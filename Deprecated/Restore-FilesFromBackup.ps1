<#
.SYNOPSIS
    Restoration utility to undo backup operations using generated file maps.

.DESCRIPTION
    Companion script to Backup-FilesAdvanced.ps1 that restores files to their original 
    locations using the file map JSON generated during backup. Handles drive letter changes,
    conflict resolution, and comprehensive error logging.

.PARAMETER FileMapPath
    Path to the FileMap JSON file generated during backup operation.

.PARAMETER DryRun
    Preview restoration without actually moving files.

.PARAMETER DriveLetterMapping
    Hashtable for drive letter changes (e.g., @{'D:' = 'E:'; 'C:' = 'F:'})

.EXAMPLE
    .\Restore-FilesFromBackup.ps1 -FileMapPath "D:\Backup\FileMap_20260109_143022.json"
    Restores files using the file map

.EXAMPLE
    .\Restore-FilesFromBackup.ps1 -FileMapPath "D:\Backup\FileMap_20260109_143022.json" -DryRun
    Preview restoration without executing

.EXAMPLE
    $mapping = @{'D:' = 'E:'}
    .\Restore-FilesFromBackup.ps1 -FileMapPath "E:\Backup\FileMap_20260109_143022.json" -DriveLetterMapping $mapping
    Restores with drive letter remapping

.NOTES
    Author: AI Engineer & PowerShell Expert
    Version: 2.0
    Last Updated: 2026-01-09
    
    This script is designed to work with the file maps generated by Backup-FilesAdvanced.ps1
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory=$true, HelpMessage="Path to the FileMap JSON file")]
    [ValidateScript({Test-Path $_ -PathType Leaf})]
    [string]$FileMapPath,

    [Parameter(Mandatory=$false, HelpMessage="Preview without executing")]
    [switch]$DryRun,

    [Parameter(Mandatory=$false, HelpMessage="Drive letter mapping for changed drives")]
    [hashtable]$DriveLetterMapping = @{}
)

